#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum OpcodeWire {
    Core(u8),
    Extended(u16),
}

const EXTENDED_OPCODE_PREFIX: u8 = 0xFF;

impl Opcode {
    #[must_use]
    pub fn family(self) -> OpcodeFamily {
        self.info().family
    }
    #[must_use]
    pub fn mnemonic(self) -> &'static str {
        self.info().mnemonic
    }
    #[must_use]
    pub fn operand_shape(self) -> OperandShape {
        self.info().operand_shape
    }
    #[must_use]
    pub fn wire_code(self) -> u16 {
        match self.info().wire {
            OpcodeWire::Core(code) => u16::from(code),
            OpcodeWire::Extended(code) => code,
        }
    }
    #[must_use]
    pub fn wire(self) -> OpcodeWire {
        self.info().wire
    }
    #[must_use]
    pub const fn extended_opcode_prefix() -> u8 {
        EXTENDED_OPCODE_PREFIX
    }
    #[must_use]
    pub fn from_mnemonic(text: &str) -> Option<Self> {
        OPCODE_INFOS
            .iter()
            .find(|info| info.mnemonic == text)
            .map(|info| info.opcode)
    }
    #[must_use]
    pub fn from_wire_code(code: u16) -> Option<Self> {
        OPCODE_INFOS
            .iter()
            .find(|info| match info.wire {
                OpcodeWire::Core(core_wire) => u16::from(core_wire) == code,
                OpcodeWire::Extended(extended) => extended == code,
            })
            .map(|info| info.opcode)
    }
    fn info(self) -> &'static OpcodeInfo {
        OPCODE_INFOS
            .iter()
            .find(|info| info.opcode == self)
            .unwrap_or(&OPCODE_INFOS[0])
    }
}

struct OpcodeInfo {
    opcode: Opcode,
    family: OpcodeFamily,
    mnemonic: &'static str,
    operand_shape: OperandShape,
    wire: OpcodeWire,
}

const fn opcode_info(
    opcode: Opcode,
    family: OpcodeFamily,
    mnemonic: &'static str,
    operand_shape: OperandShape,
    code: u8,
) -> OpcodeInfo {
    OpcodeInfo {
        opcode,
        family,
        mnemonic,
        operand_shape,
        wire: OpcodeWire::Core(code),
    }
}

